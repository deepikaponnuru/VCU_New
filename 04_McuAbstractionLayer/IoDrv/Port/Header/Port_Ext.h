#ifndef PORT_EXT_H_
#define PORT_EXT_H_
/*------------------------------------------------------------------ Standard Types Include ---------------------------------------------------------------*/
#include "driverlib.h"
#include "device.h"
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------- Global Typedefs -----------------------------------------------------------------*/
/* Pad types mapped to TI macros */
typedef enum {
    PIO_PIN_TYPE_STD       = GPIO_PIN_TYPE_STD,
    PIO_PIN_TYPE_PULLUP    = GPIO_PIN_TYPE_PULLUP,
    PIO_PIN_TYPE_OPENDRAIN = GPIO_PIN_TYPE_OD,
    PIO_PIN_TYPE_INVERT    = GPIO_PIN_TYPE_INVERT
} Port_PinType;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------- Global Typedefs -----------------------------------------------------------------*/
/* Pin state */
typedef enum {
    PIN_STATE_LOW  = 0,
    PIN_STATE_HIGH = 1
} Port_PinState;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------- Global Typedefs -----------------------------------------------------------------*/
/* GPIO configuration structure */
typedef struct {
    uint16_t idx;              /* GPIO number */
    uint32_t pinConfig;        /* GPIO_x_GPIOx */
    GPIO_Direction direction;  /* GPIO_DIR_MODE_IN / OUT */
    Port_PinType pintype;      /* Pad configuration */
    Port_PinState state;       /* Initial state */
} Port_PinConfigStruct_t;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------*/
 
/*------------------------------------------------------- Global Interface Functions Declaration ----------------------------------------------------------*/
void Port_Init(void);
/*---------------------------------------------------------------------------------------------------------------------------------------------------------*/
#endif /* PORT_EXT_H_ */
