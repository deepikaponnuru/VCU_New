/*---------------------------------------------------------------------------------------------------------------------------------------------------------*/
Dio_LevelType Dio_ReadChannel(Dio_ChannelType ChannelId)
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------
Function Name   : Dio_ReadChannel

Description     : This function reads the Dio_LevelType for the specified ChannelId

In Parameter    : Digital Channel Id

Out Parameter   : N/A

In-Out Parameter: N/A

Return          : Digital Level Type (High / Low)
  			  
-----------------------------------------------------------------------------------------------------------------------------------------------------------*/
{  
    return GPIO_readPin(ChannelId);
    
}

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------*/
void Dio_WriteChannel(Dio_ChannelType ChannelId, Dio_LevelType Level)
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------
Function Name   : Dio_WriteChannel

Description     : This function writes the Digital Level to the specified ChannelId

In Parameter    : Digital Channel Id, Digital Level (High / Low)

Out Parameter   : N/A

In-Out Parameter: N/A

Return          : N/A
  			  
-----------------------------------------------------------------------------------------------------------------------------------------------------------*/
{
    if (Level == STD_HIGH)
    {
        GPIO_writePin(ChannelId, 1U);
    }
    else if(Level == STD_LOW)
    {
        GPIO_writePin(ChannelId, 0U);
    }
}
